<template>
  <el-dialog
    :title="!dataForm.id ? '新增' : '修改'"
    :close-on-click-modal="false"
    :visible.sync="visible"
  >
    <el-form
      :model="dataForm"
      :rules="dataRule"
      ref="dataForm"
      @keyup.enter.native="dataFormSubmit()"
      label-width="80px"
    >
      <el-form-item label="医院名称" prop="hospitalName">
        <el-input v-model="dataForm.hospitalName" placeholder></el-input>
      </el-form-item>
      <el-form-item label="状态" prop="status">
        <template>
          <el-radio v-model="dataForm.status" label="1">启用</el-radio>
          <el-radio v-model="dataForm.status" label="0">禁用</el-radio>
        </template>
      </el-form-item>
    </el-form>
    <span slot="footer" class="dialog-footer">
      <el-button @click="visible = false">取消</el-button>
      <el-button type="primary" @click="dataFormSubmit()">确定</el-button>
    </span>
  </el-dialog>
</template>

<script>
export default {
  data() {
    return {
      visible: false,
      dataForm: {
        id: 0,
        hospitalName: "",
        createTime: "",
        createUserId: "",
        status: "1"
      },
      dataRule: {
        hospitalName: [
          { required: true, message: "不能为空", trigger: "blur" }
        ],
        status: [{ required: true, message: "不能为空", trigger: "blur" }]
      }
    };
  },
  methods: {
    init(id) {
      this.dataForm.id = id || 0;
      this.visible = true;
      this.$nextTick(() => {
        this.$refs["dataForm"].resetFields();
        if (this.dataForm.id) {
          this.$http({
            url: this.$http.adornUrl(`/tb/hospital/info/${this.dataForm.id}`),
            method: "get",
            params: this.$http.adornParams()
          }).then(({ data }) => {
            if (data && data.code === 0) {
              this.dataForm.hospitalName = data.hospital.hospitalName;
              this.dataForm.createTime = data.hospital.createTime;
              this.dataForm.createUserId = data.hospital.createUserId;
              this.dataForm.status = data.hospital.status + "";
            }
          });
        }
      });
    },
    // 表单提交
    dataFormSubmit() {
      this.$refs["dataForm"].validate(valid => {
        if (valid) {
          this.$http({
            url: this.$http.adornUrl(
              `/tb/hospital/${!this.dataForm.id ? "save" : "update"}`
            ),
            method: "post",
            data: this.$http.adornData({
              id: this.dataForm.id || undefined,
              hospitalName: this.dataForm.hospitalName,
              createTime: this.dataForm.createTime,
              createUserId: this.dataForm.createUserId,
              status: this.dataForm.status
            })
          }).then(({ data }) => {
            if (data && data.code === 0) {
              this.$message({
                message: "操作成功",
                type: "success",
                duration: 1500,
                onClose: () => {
                  this.visible = false;
                  this.$emit("refreshDataList");
                }
              });
            } else {
              this.$message.error(data.msg);
            }
          });
        }
      });
    }
  }
};
</script>
